namespace ExpressionEngine
{
    /// <summary>
    /// Represents various angle systems
    /// </summary>
    public enum AngleMode
    {
        /// <summary>
        /// Degrees. 1 Circle = 360 degrees
        /// </summary>
        Deg,
        /// <summary>
        /// Radians. 1 Circle = 2pi rads
        /// </summary>
        Rad,
        /// <summary>
        /// Gradians. 1 Circle = 400 gradians
        /// </summary>
        Grad,
    }
}
